package TP2D;

public class TileManager {
    private final int width;
    private final int heigth;

    public TileManager(int width, int height) {
        this.width = width;
        this.heigth = height;
    }

    public int getWidth() {
        return width;
    }

    public int getHeigth() {
        return heigth;
    }
}
